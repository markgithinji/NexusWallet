@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun WalletCreationScreen(
    onNavigateUp: () -> Unit,
    onNavigateToMain: () -> Unit,
    viewModel: WalletCreationViewModel = hiltViewModel()
) {
    val uiState by viewModel.uiState.collectAsState()
    val currentStep by viewModel.currentStep.collectAsState()
    val mnemonic by viewModel.mnemonic.collectAsState()
    val coinSelection by viewModel.coinSelection.collectAsState()
    val walletName by viewModel.walletName.collectAsState()
    val isMnemonicGenerated by viewModel.isMnemonicGenerated.collectAsState()
    val enteredWords by viewModel.enteredWords.collectAsState()

    // Track if user has seen the mnemonic warning
    var hasSeenSecurityWarning by remember { mutableStateOf(false) }

    // Handle navigation when wallet is created
    LaunchedEffect(uiState) {
        if (uiState is WalletCreationUiState.WalletCreated) {
            onNavigateToMain()
        }
    }

    Scaffold(
        topBar = {
            TopAppBar(
                title = {
                    Text(
                        "Create Wallet",
                        color = MaterialTheme.colorScheme.onSurface
                    )
                },
                navigationIcon = {
                    if (currentStep > 0) {
                        IconButton(onClick = {
                            if (currentStep == 1 && !hasSeenSecurityWarning) {
                                onNavigateUp()
                            } else {
                                viewModel.previousStep()
                            }
                        }) {
                            Icon(
                                Icons.Default.ArrowBack,
                                "Back",
                                tint = MaterialTheme.colorScheme.onSurfaceVariant
                            )
                        }
                    }
                },
                colors = TopAppBarDefaults.topAppBarColors(
                    containerColor = MaterialTheme.colorScheme.surface
                )
            )
        }
    ) { padding ->
        if (currentStep < 4 && uiState is WalletCreationUiState.Loading) {
            LoadingScreen()
            return@Scaffold
        }

        if (currentStep < 4 && uiState is WalletCreationUiState.Error) {
            ErrorScreen(
                message = (uiState as WalletCreationUiState.Error).message,
                onRetry = { viewModel.reset() }
            )
            return@Scaffold
        }

        WalletCreationStepper(
            currentStep = currentStep,
            padding = padding,
            content = {
                when (currentStep) {
                    0 -> CoinSelectionStep(
                        coinSelection = coinSelection,
                        onSelectionChange = { updatedSelection ->
                            viewModel.updateCoinSelection(
                                includeBitcoinMainnet = updatedSelection.includeBitcoinMainnet,
                                includeBitcoinTestnet = updatedSelection.includeBitcoinTestnet,
                                includeEthereumMainnet = updatedSelection.includeEthereumMainnet,
                                includeEthereumSepolia = updatedSelection.includeEthereumSepolia,
                                includeSolanaMainnet = updatedSelection.includeSolanaMainnet,
                                includeSolanaDevnet = updatedSelection.includeSolanaDevnet,
                                includeUSDCMainnet = updatedSelection.includeUSDCMainnet,
                                includeUSDCSepolia = updatedSelection.includeUSDCSepolia,
                                includeUSDTMainnet = updatedSelection.includeUSDTMainnet
                            )
                        },
                        onNext = { viewModel.nextStep() }
                    )

                    1 -> {
                        if (!hasSeenSecurityWarning) {
                            SecurityWarningDialog(
                                onAccept = {
                                    hasSeenSecurityWarning = true
                                    if (!isMnemonicGenerated) {
                                        viewModel.generateMnemonic()
                                    }
                                },
                                onCancel = { viewModel.previousStep() }
                            )
                        }

                        if (hasSeenSecurityWarning) {
                            if (mnemonic.isNotEmpty()) {
                                MnemonicDisplayStep(
                                    mnemonic = mnemonic,
                                    onNext = { viewModel.nextStep() },
                                    onBack = { viewModel.previousStep() }
                                )
                            } else {
                                Box(
                                    modifier = Modifier.fillMaxSize(),
                                    contentAlignment = Alignment.Center
                                ) {
                                    Column(
                                        horizontalAlignment = Alignment.CenterHorizontally
                                    ) {
                                        CircularProgressIndicator(
                                            color = MaterialTheme.colorScheme.primary
                                        )
                                        Spacer(modifier = Modifier.height(16.dp))
                                        Text(
                                            "Generating secure recovery phrase...",
                                            color = MaterialTheme.colorScheme.onSurfaceVariant
                                        )
                                    }
                                }
                            }
                        }
                    }

                    2 -> MnemonicVerificationStep(
                        mnemonic = mnemonic,
                        enteredWords = enteredWords,
                        onAddWord = { word -> viewModel.addWordToVerification(word) },
                        onRemoveWord = { index -> viewModel.removeWordFromVerification(index) },
                        onVerify = {
                            if (viewModel.completeVerificationAndMoveNext()) {
                                viewModel.nextStep()
                            }
                        },
                        onBack = { viewModel.previousStep() }
                    )

                    3 -> WalletNameStep(
                        walletName = walletName,
                        onNameChange = { viewModel.setWalletName(it) },
                        onCreate = { viewModel.createWallet() }
                    )

                    4 -> {
                        when (uiState) {
                            is WalletCreationUiState.WalletCreated -> {
                                val wallet = (uiState as WalletCreationUiState.WalletCreated).wallet
                                WalletSuccessStep(
                                    wallet = wallet,
                                    onFinish = onNavigateToMain
                                )
                            }
                            is WalletCreationUiState.Loading -> {
                                Box(
                                    modifier = Modifier.fillMaxSize(),
                                    contentAlignment = Alignment.Center
                                ) {
                                    Column(
                                        horizontalAlignment = Alignment.CenterHorizontally
                                    ) {
                                        CircularProgressIndicator(
                                            color = MaterialTheme.colorScheme.primary
                                        )
                                        Spacer(modifier = Modifier.height(16.dp))
                                        Text(
                                            "Creating wallet...",
                                            color = MaterialTheme.colorScheme.onSurfaceVariant
                                        )
                                    }
                                }
                            }
                            else -> {
                                LaunchedEffect(Unit) {
                                    viewModel.previousStep()
                                }
                                LoadingScreen()
                            }
                        }
                    }
                }
            }
        )
    }
}

@Composable
fun WalletCreationStepper(
    currentStep: Int,
    padding: PaddingValues,
    content: @Composable () -> Unit
) {
    val steps = listOf("Coins", "Backup", "Verify", "Name", "Complete")
    val stepDescriptions = remember(currentStep) {
        when (currentStep) {
            0 -> "Select which cryptocurrencies and networks to include"
            1 -> "Backup your recovery phrase"
            2 -> "Verify your backup"
            3 -> "Personalize your wallet"
            4 -> "Wallet created successfully"
            else -> ""
        }
    }

    Column(
        modifier = Modifier
            .fillMaxSize()
            .padding(padding)
            .background(MaterialTheme.colorScheme.background)
    ) {
        Column(
            modifier = Modifier
                .fillMaxWidth()
                .padding(horizontal = 16.dp, vertical = 12.dp)
        ) {
            Text(
                text = "Step ${currentStep + 1} of ${steps.size}",
                style = MaterialTheme.typography.labelMedium,
                color = MaterialTheme.colorScheme.primary,
                fontWeight = FontWeight.Medium
            )

            Spacer(modifier = Modifier.height(4.dp))

            Text(
                text = stepDescriptions,
                style = MaterialTheme.typography.bodyMedium,
                color = MaterialTheme.colorScheme.onSurfaceVariant
            )
        }

        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(horizontal = 24.dp, vertical = 8.dp),
            horizontalArrangement = Arrangement.SpaceBetween
        ) {
            steps.forEachIndexed { index, step ->
                StepIndicator(
                    stepNumber = index + 1,
                    stepName = step,
                    isActive = index == currentStep,
                    isCompleted = index < currentStep,
                    isNext = index == currentStep + 1
                )
            }
        }

        LinearProgressIndicator(
            progress = { (currentStep + 1) / steps.size.toFloat() },
            modifier = Modifier
                .fillMaxWidth()
                .padding(horizontal = 16.dp, vertical = 4.dp)
                .height(4.dp),
            color = MaterialTheme.colorScheme.primary,
            trackColor = MaterialTheme.colorScheme.surfaceVariant
        )

        Spacer(modifier = Modifier.height(24.dp))

        Box(
            modifier = Modifier
                .fillMaxSize()
                .padding(horizontal = 16.dp)
        ) {
            content()
        }
    }
}

